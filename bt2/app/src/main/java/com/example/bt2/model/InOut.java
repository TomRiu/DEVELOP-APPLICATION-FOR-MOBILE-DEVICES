package com.example.bt2.model;

public class InOut {
    private int id;
    private String name;

    public InOut() {
    }

    public InOut(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
